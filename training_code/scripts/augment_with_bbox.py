import os
import cv2
import numpy as np
import random


def extract_objects_from_image(img_path, mask_path):
    """Extract all polygon objects from a single image as tight crops"""
    objects = []
    img = cv2.imread(img_path)
    mask = cv2.imread(mask_path)
    if img is None or mask is None:
        return objects

    for color in TARGET_COLORS:
        lower = np.array(color, dtype=np.uint8)
        upper = np.array(color, dtype=np.uint8)
        color_mask = cv2.inRange(mask, lower, upper)
        contours, _ = cv2.findContours(color_mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

        for cnt in contours:
            if cv2.contourArea(cnt) < 50:
                continue
            poly_mask = np.zeros_like(color_mask)
            cv2.drawContours(poly_mask, [cnt], -1, 255, -1)
            final_mask = cv2.bitwise_and(color_mask, poly_mask)
            x, y, w, h = cv2.boundingRect(cnt)
            obj_img = img[y:y + h, x:x + w]
            obj_mask = final_mask[y:y + h, x:x + w]
            objects.append((obj_img, obj_mask, color))
    return objects


def create_synthetic_image(image_list, background_img_dir, background_mask_dir,
                           save_prefix, input_img_dir, input_mask_dir,
                           out_img_dir, out_mask_dir):
    h, w = CANVAS_SIZE

    # pick a random background image
    files = [f for f in os.listdir(background_img_dir) if f.lower().endswith(".jpg")]
    bg_name = random.choice(files)
    synth_image_path = os.path.join(background_img_dir, bg_name)

    # read matching background mask with same filename but .png extension
    mask_name = os.path.splitext(bg_name)[0] + ".png"
    synth_mask_path = os.path.join(background_mask_dir, mask_name)

    synth_img = cv2.imread(synth_image_path)
    synth_mask = cv2.imread(synth_mask_path)

    # if mask not found, create blank
    if synth_mask is None:
        synth_mask = np.zeros_like(synth_img)

    objects = []
    selected_images = random.sample(image_list, k=min(len(image_list), 10))
    for name in selected_images:
        img_path = os.path.join(input_img_dir, f"{name}.jpg")
        mask_path = os.path.join(input_mask_dir, f"{name}.png")
        if os.path.exists(img_path) and os.path.exists(mask_path):
            objs = extract_objects_from_image(img_path, mask_path)
            objects.extend(objs)

    if len(objects) == 0:
        return

    selected_objs = random.sample(objects, min(OBJECTS_PER_IMAGE, len(objects)))

    for obj_img, obj_mask, color in selected_objs:
        oh, ow = obj_img.shape[:2]
        if oh >= h or ow >= w:
            continue
        x_offset = random.randint(0, w - ow)
        y_offset = random.randint(0, h - oh)
        mask_bool = obj_mask > 0
        synth_img[y_offset:y_offset + oh, x_offset:x_offset + ow][mask_bool] = obj_img[mask_bool]
        synth_mask[y_offset:y_offset + oh, x_offset:x_offset + ow][mask_bool] = color

    os.makedirs(out_img_dir, exist_ok=True)
    os.makedirs(out_mask_dir, exist_ok=True)
    cv2.imwrite(os.path.join(out_img_dir, f"{save_prefix}.jpg"), synth_img)
    cv2.imwrite(os.path.join(out_mask_dir, f"{save_prefix}.png"), synth_mask)


